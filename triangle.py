class triangle:
    def area():
        base = 3
        height = 5
        return (base*height)/2
    
    def perimeter():
        base = 3
        height = 5
        return base+height+height

triangle = triangle
area = triangle.area()
perimeter = triangle.perimeter()

print(f"Area of the triangle: {area}")
print(f"Perimeter of the triangle: {perimeter}")